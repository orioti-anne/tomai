def get_base_date_time():
    """초단기예보 생성 시각(45분)을 고려한 기준 시간 계산"""
    now = datetime.now()
    if now.minute < 45:
        target = now - timedelta(hours=1)
        return target.strftime("%Y%m%d"), target.strftime("%H30")
    return now.strftime("%Y%m%d"), now.strftime("%H30")

def get_weather(region_l1, region_l2):
    """지역명을 입력받아 현재 날씨 정보를 반환"""
    if not region_l1 or not region_l2:
        return None

    r1, r2 = str(region_l1).strip(), str(region_l2).strip()
    grid = REGION_GRID.get((r1, r2))

    if not grid:
        print(f"격자 좌표 없음: '{r1}', '{r2}'")
        return None

    nx, ny = grid
    base_date, base_time = get_base_date_time()

    url = "http://apis.data.go.kr/1360000/VilageFcstInfoService_2.0/getUltraSrtFcst"
    params = {
        "serviceKey": SERVICE_KEY,
        "pageNo": "1",
        "numOfRows": "100",
        "dataType": "JSON",
        "base_date": base_date,
        "base_time": base_time,
        "nx": nx,
        "ny": ny,
    }

    try:
        response = requests.get(url, params=params, timeout=10)
        response.raise_for_status()
        data = response.json()

        header = data.get("response", {}).get("header", {})
        if header.get("resultCode") != "00":
            return None

        items = data.get("response", {}).get("body", {}).get("items", {}).get("item", [])
        if not items:
            return None

        first_fcst_time = items[0].get("fcstTime")
        res = {"temp": None, "sky": "정보 없음", "icon": "cloud-sun"}

        for item in items:
            if item.get("fcstTime") != first_fcst_time:
                continue

            category, value = item.get("category"), item.get("fcstValue")

            if category == "T1H":
                res["temp"] = value
            elif category == "SKY":
                mapping = {"1": ("맑음", "sun"), "3": ("구름 많음", "cloud-sun"), "4": ("흐림", "cloud")}
                if value in mapping:
                    res["sky"], res["icon"] = mapping[value]
            elif category == "PTY":
                if value == "1":
                    res["sky"], res["icon"] = ("비", "cloud-rain")
                elif value in ["2", "3"]:
                    res["sky"], res["icon"] = ("눈/비", "cloud-snow")
                elif value == "4":
                    res["sky"], res["icon"] = ("소나기", "cloud-rain")

        return {
            "region_name": f"{r1} {r2}",
            "temp": res["temp"],
            "sky": res["sky"],
            "icon": res["icon"],
            "base_time": f"{base_date} {base_time}"
        }

    except Exception:
        return None


